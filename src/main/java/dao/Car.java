package dao;

/**
 *
 * @author Kamalion
 */
public class Car {
    
    private String Id;
    private String Brand;
    private int Year;
    
    public Car(String Id, String Brand, int Year){
        this.Id = Id;
        this.Brand = Brand;
        this.Year = Year;
        
    }

    /**
     * @return the Id
     */
    public String getId() {
        return Id;
    }

    /**
     * @param Id the Id to set
     */
    public void setId(String Id) {
        this.Id = Id;
    }

    /**
     * @return the Brand
     */
    public String getBrand() {
        return Brand;
    }

    /**
     * @param Brand the Brand to set
     */
    public void setBrand(String Brand) {
        this.Brand = Brand;
    }

    /**
     * @return the Year
     */
    public int getYear() {
        return Year;
    }

    /**
     * @param Year the Year to set
     */
    public void setYear(int Year) {
        this.Year = Year;
    }
        
    
}
